    private void lblThoiGianMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblThoiGianMouseClicked
        if (timer.isRunning()) {
            timer.stop();
            this.lblTrangThai.setText("Clock time is stopping: " + new Date(evt.getWhen()));
        } else {
            timer.start();
            this.lblTrangThai.setText("Continue times..." + new Date(evt.getWhen()));
        }
    }//GEN-LAST:event_lblThoiGianMouseClicked

