    private Timer timer = null;

    private void initData() {
        timer = Util.setTimer(lblThoiGian, "hh:mm:ss aa");
        timer.start();
        this.setLocationRelativeTo(null);
        this.toFront();
    }

    // Thoát chương trình đăng hiện tại
    public void ExitForm(String title) {
        // MsgBox trả về "0":có - "1":không
        if (MsgBox.confirm(this, "Thoát chương trình \""
                + title + "\"?", title, MsgBox.CanhBao) == 0) {
            System.exit(0);
        }
    }

    private void ThongKeSelected(int table) {
        ThongKeJFrame thongKe = new ThongKeJFrame(this);
        thongKe.getTabsTHTK().setSelectedIndex(table);
        this.setForm(thongKe);
    }

    private void setForm(javax.swing.JFrame frame) {
        frame.setVisible(rootPaneCheckingEnabled);
        frame.setFocusCycleRoot(rootPaneCheckingEnabled);
        frame.doLayout();
        this.lblTrangThai.setText(frame.getTitle());
    }

    private void openWebSide(String path, String mes) {
        try {
            Desktop.getDesktop().browse(new File(path).toURI());
        } catch (IOException ex) {
            System.out.println(ex.getMessage());
            MsgBox.alert(this, "Không tìm thấy trang!", mes, MsgBox.KhongCo);
        }
    }
